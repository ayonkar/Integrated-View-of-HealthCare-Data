    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
        
        Organization organization = (Organization) organizationEmpJComboBox.getSelectedItem();
        String name = nameJTextField.getText();
        String designation = designationJTextField.getText();
        organization.getEmployeeDirectory().createEmployee(name,designation,maxId);
        
        if(organization instanceof DrugDiscoveryOrganisation   ){
        DrugDiscoveryOrganisation drugdd = (DrugDiscoveryOrganisation) organization;
        try {
            drugdd.setDd(ConfigureASystem.configureDisease());
            
            //organization.setDd();
        } catch (ParseException ex) {
            Logger.getLogger(ManageEmployeeJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        
        if( organization instanceof NewDrugApprovalOrganisation  ){
        NewDrugApprovalOrganisation newDrugAo = (NewDrugApprovalOrganisation) organization;
        try {
            newDrugAo.setDiseasediretory(ConfigureASystem.configureDisease());
            
            //organization.setDd();
        } catch (ParseException ex) {
            Logger.getLogger(ManageEmployeeJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        }
        
           if( organization instanceof ClinicalRegulatoryOrganisation  ){
        ClinicalRegulatoryOrganisation regulatoryBody = (ClinicalRegulatoryOrganisation) organization;
        try {
            regulatoryBody.setDiseasediretory(ConfigureASystem.configureDisease());
            
            //organization.setDd();
        } catch (ParseException ex) {
            Logger.getLogger(ManageEmployeeJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        }
        
          if(organization instanceof HospitalOrganization ){
        HospitalOrganization ho = (HospitalOrganization) organization;
            ho.setHd(ConfigureASystem.configureHospital());

        }
        if(organization instanceof ClinicOrganization ){
        ClinicOrganization ho = (ClinicOrganization) organization;
            ho.setCd(ConfigureASystem.configureClinic());

        }
        if(organization instanceof PharmacyOrganization ){
        PharmacyOrganization ho = (PharmacyOrganization) organization;
            ho.setPd(ConfigureASystem.configurePharmacy());

        }
           
      
        
        
        
        
        
        
        
    }//GEN-LAST:event_addJButtonActionPerformed

