    private void removeEmployeejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeEmployeejButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = organizationJTable.getSelectedRow();
        if(selectedRow >= 0)
        {
            int dialogButon = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null,"Would you like to delete the employee details","",dialogButon);
            if(dialogResult == JOptionPane.YES_OPTION)
            {
                Employee e = (Employee) organizationJTable.getValueAt(selectedRow,2);
                Organization organization = (Organization) organizationJComboBox.getSelectedItem();
                
                organization.getEmployeeDirectory().removeEmployee(e);
                
                populateTable(organization);
                JOptionPane.showMessageDialog(null,"Employer sucessfully deleted!");
            }
        }
        else
        {
                JOptionPane.showMessageDialog(null,"Please Select a Supplier to deleted"); 
        }
        
    
        
        
        
        
    }//GEN-LAST:event_removeEmployeejButtonActionPerformed

