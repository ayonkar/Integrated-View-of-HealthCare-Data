    private void requestTestjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestTestjButtonActionPerformed
        // TODO add your handling code here:

        if (!(selectedChemicalsjComboBox.getItemCount() == 0)) {

            DrugDiscoveryOrganisation drugdd = (DrugDiscoveryOrganisation) organization;
            WorkQueue wq = drugdd.getWorkQueue();
            LabTestWorkRequest request = new LabTestWorkRequest();
            if (diseasejRadioButton.isSelected()) {
                request.setDge("Disease");
            } else {
                request.setDge("Healthy");
            }

            request.setSender(userAccount);
            request.setStatus("Sent");

            TestDrug td = new TestDrug();
            td.setTcd(tcd);
            td.setDiseasename(String.valueOf(diseasejComboBox.getSelectedItem()));

            request.setTestdrug(td);

            Organization org = null;
            for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                if (organization instanceof ClinicalTrialsOrganisation) {
                    org = organization;
                    break;
                }
            }
            if (org != null) {
                org.getWorkQueue().getWorkRequestList().add(request);
                userAccount.getWorkQueue().getWorkRequestList().add(request);
            }

            JOptionPane.showMessageDialog(null, "Experiment Requested ");
        } else {
            JOptionPane.showMessageDialog(null, "Please Choose Chemical for the Experiment");
        }


    }//GEN-LAST:event_requestTestjButtonActionPerformed

