    private void chemicalmolWeightJtextKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_chemicalmolWeightJtextKeyPressed
        int keyp = evt.getKeyCode();

        if (keyp == 46) {
            keyp = 110;
        }
        if ((keyp == KeyEvent.VK_BACKSPACE) || (keyp >= evt.VK_0 && keyp <= evt.VK_9) || (keyp == evt.VK_DECIMAL)) {
            chemicalmolWeightJtext.setEditable(true);
            chemicalmolWeightJtext.setBackground(Color.YELLOW);

        } else {
            chemicalmolWeightJtext.setEditable(false);
            chemicalmolWeightJtext.setBackground(Color.red);
            getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Special Characters Prohibited ");
            chemicalmolWeightJtext.setBackground(Color.WHITE);
            chemicalmolWeightJtext.requestFocusInWindow();
        }
    }//GEN-LAST:event_chemicalmolWeightJtextKeyPressed

