    private void populateComboBox() {
        diseasejComboBox.removeAllItems();
        DrugDiscoveryOrganisation drugdd = (DrugDiscoveryOrganisation) organization;
        for (Disease disease : drugdd.getDd().getDisease()) {
            diseasejComboBox.addItem(disease);
        }
    }

    private void populateOtherChemicalList() {
        DrugDiscoveryOrganisation drugdd = (DrugDiscoveryOrganisation) organization;
        int selectedRow = drugListjTable.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) otherChemicaListjTable.getModel();

        model.setRowCount(0);
        for (Disease disease : drugdd.getDd().getDisease()) {
            for (AvailableDrug drug : disease.getAvalaibleDrug()) {
                AvailableDrug availdrug = (AvailableDrug) drugListjTable.getValueAt(selectedRow, 1);
                if (drug.equals(availdrug)) {
                    break;
                }
                //     if (organization instanceof LabOrganization){
                // break;    

                for (Chemical chemical : drug.getChemicalList().getChemical()) {

                    Object[] row = new Object[5];
                    row[0] = chemical.getChemicalID();
                    row[1] = chemical;
                    row[2] = chemical.getMolecularWeight();
                    row[3] = chemical.getFormula();

                    model.addRow(row);
                }
            }
        }

    }

