    private void populateTable(NewDiscoveredDrug newDrug) {

        DefaultTableModel model = (DefaultTableModel) chemicaljTable.getModel();

        model.setRowCount(0);
        ///System.out.println("Size is " + newDrug.getTcd().getChemical().size());

        for (Chemical chemical : newDrug.getTcd().getChemical()) {

            Object[] row = new Object[7];
            row[0] = chemical.getChemicalID();
            row[1] = chemical.getChemicalName();
            row[2] = chemical.getFormula();
            row[3] = chemical.getMolecularWeight();

            model.addRow(row);

        }

    }
