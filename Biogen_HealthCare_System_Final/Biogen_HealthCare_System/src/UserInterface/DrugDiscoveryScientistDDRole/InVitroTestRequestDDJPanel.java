    private void requestTestjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestTestjButtonActionPerformed
        // TODO add your handling code here:

//        if (JOptionPane.showConfirmDialog(null, "please Confirm?", "Information",
//        JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION)
        try {
            String EMPTY = "";
            if (exposurejTextField == null || wavelengthjTextField == null || exposurejTextField.getText().equals(EMPTY) || wavelengthjTextField.getText().equals(EMPTY)) {
                JOptionPane.showMessageDialog(null, " Please Enter all the experiment setup details ");
            } 
            else {

                // yes option
                priClinicalwr.setTotalSetTitter(Integer.parseInt(String.valueOf(totalsetjSpinner.getValue())));
                priClinicalwr.setExperimentTime(Integer.parseInt(String.valueOf(expermentTimejSpinner.getValue())));
                priClinicalwr.setTemperature(Integer.parseInt(String.valueOf(temperaturejSlider.getValue())));
                priClinicalwr.setExposure(Integer.valueOf(exposurejTextField.getText()));
                priClinicalwr.setWavelength(Integer.valueOf(wavelengthjTextField.getText()));
                priClinicalwr.setLysistime(Integer.parseInt(String.valueOf(lysistimejSlider.getValue())));
                priClinicalwr.setPriority(String.valueOf(priorityjSlider.getValue()));
                Calendar choosenDate = jDateChooser.getCalendar();
                Calendar currentDate = Calendar.getInstance();
                if ((choosenDate == null) || choosenDate.compareTo(currentDate) < 0) {

                    jDateChooser.setBackground(Color.red);
                    JOptionPane.showMessageDialog(null, " Invalid Date");
                    jDateChooser.setBackground(Color.white);

                    
                }
                else{

                priClinicalwr.setEstimatedDayofDelivery(jDateChooser.getDate());
                priClinicalwr.setDrugID(newDrug.getDrugID());
                //  System.out.println("DrugNmae in PriClinical " + newDrug.getDrugName());
                priClinicalwr.setDrugName(newDrug.getDrugName());
                //  priClinicalwr.setStatus("Sent");
                priClinicalwr.setDiseaseName(newDrug.getDiseasename());

                Organization org = null;
                for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    if (organization instanceof ClinicalTrialsOrganisation) {
                        org = organization;
                        break;
                    }
                }
                if (org != null) {
                    ClinicalTrialsOrganisation cto = (ClinicalTrialsOrganisation) org;

                    cto.getPriclinicalWorkQueue().getWorkRequestList().add(priClinicalwr);
                    // userAccount.getWorkQueue().getWorkRequestList().add(request);
                }
                JOptionPane.showMessageDialog(null, "Experiment  Requested ");
                }

                
            }
        } catch (Exception e) {
            System.out.println(" Input invalid ");
            JOptionPane.showMessageDialog(null, " Please Enter all the experiment setup details ");
        }

    }//GEN-LAST:event_requestTestjButtonActionPerformed

