    public void populateTiterTable() {

        DefaultTableModel model = (DefaultTableModel) titerSetupjTable.getModel();

        model.setRowCount(0);

        for (TiterTest titer : priClinicalwr.getTiterPlate()) {
            Object[] row = new Object[3];

            row[0] = titer;
            row[1] = titer.getMediumQuantity();
            row[2] = titer.getTotalNumberofAssay();
            model.addRow(row);
        }

    }

