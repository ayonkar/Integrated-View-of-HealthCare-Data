    private void startjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startjButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = titerSetupjTable.getSelectedRow();
          if (selectedRow >= 0) {
                   jProgressBar.setValue(0);
                 Thread progressBar = new Thread() {
            public void run() {
                for (int t = 0 ;t < 5;t++ ) {
                   
                    try {
                         
                         jProgressBar.setValue(y);
                        sleep(100);
                       populate();
                         System.out.println("Value of Progress Bar "+jProgressBar.getValue());

                    } catch (Exception e) {
                        JOptionPane.showConfirmDialog(null, "Progress Bar error");
                    }

                }
                JOptionPane.showMessageDialog(null,"Test Completed");
                populatey();
                
                
                   TiterTest tt = (TiterTest) titerSetupjTable.getValueAt(selectedRow, 0);
                   tt.setTiterTest("Done");
                   Random r = new Random();
                   Double sample = r.nextDouble() * 100;
                   tt.setCellViability((sample));
                   tt.setRos(sample*.8);
                   
                   
                    DecimalFormat df = new DecimalFormat("#.##");
                    df.setRoundingMode(RoundingMode.CEILING);
                   
                   rosjTextField.setText(String.valueOf(df.format(sample)));
                   cellViajTextField.setText(String.valueOf(df.format(sample*0.8)));
                           
                
                
   
            }
                 };
                 progressBar.start();
          }
          else
                {
                   JOptionPane.showMessageDialog(null,"Please Select the titer Plate Number from the Table ");
                }
        
    }//GEN-LAST:event_startjButtonActionPerformed

    
