    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        
        try{Date resolutionDate = jDateChooser.getCalendar().getTime();
        Calendar currentDate = Calendar.getInstance();
        if((resolutionDate == null) || resolutionDate.compareTo(lwr.getRequestDate()) < 0){
            
            jDateChooser.setBackground(Color.red);
            JOptionPane.showMessageDialog(null," Invalid Date");
            jDateChooser.setBackground(Color.white);
          
        }  
        
        else{
        lwr.setResolveDate(jDateChooser.getDate());
        
        if (passjRadioButton.isSelected())
        {
            lwr.setTestResult("Pass");
        }
        else
            lwr.setTestResult("Failed");
        
        
        lwr.getTestdrug().setAcitivity(activityjTextField.getText());
        lwr.getTestdrug().setAreaunderCurve(Double.valueOf(aucjTextField.getText()));
        lwr.getTestdrug().setAbsorbility(Double.valueOf(absorbilityjTextField.getText()));
        lwr.setScore(Double.valueOf(scorejTextField.getText()));
        lwr.setStatus("Completed");
        
        JOptionPane.showMessageDialog(null,"Test Request Completed and Sent ");
        }
        }
        catch(Exception e ){JOptionPane.showMessageDialog(null, "Please enter all Result Fields  ");}
    }//GEN-LAST:event_submitJButtonActionPerformed

