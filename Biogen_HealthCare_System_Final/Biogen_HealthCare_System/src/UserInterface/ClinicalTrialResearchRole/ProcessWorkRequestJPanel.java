    private void populateScorejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_populateScorejButtonActionPerformed
        // TODO add your handling code here:
        
        int selectedColumn = scorejTable.getSelectedColumn();

        DecimalFormat df = new DecimalFormat("#.##");
        df.setRoundingMode(RoundingMode.CEILING);
        
        
        
        
        if (selectedColumn >= 0) {
            TestingTechnique testingmethod = (TestingTechnique) scorejTable.getValueAt(0,selectedColumn);
            ;
            scorejTextField.setText(String.valueOf(df.format(testingmethod.getScore())));
            absorbilityjTextField.setText(String.valueOf(df.format(testingmethod.getAbsorbility())));
            aucjTextField.setText(String.valueOf(df.format(testingmethod.getAuc())));
        } else {
            JOptionPane.showMessageDialog(null, "Please Select a Method to populate Score !");
        }
        

        
    }//GEN-LAST:event_populateScorejButtonActionPerformed

    public void populateTable(IntregatedNetworkTest test) {
        DefaultTableModel model = (DefaultTableModel) scorejTable.getModel();

        model.setRowCount(0);

         
            Object[] row = new Object[4];
            row[0] = (NetworkPropagation) test.getNp();
            row[1] = (RandomWalk)test.getRw();
            row[2] = (InterConnectivity) test.getIc();
            row[3] = (NeighborhoodScoring) test.getNs();

            model.addRow(row);
    }
    
